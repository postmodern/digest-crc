  def self.create_new_class_from(parent_class)
    # Class name without module
    parent_name = parent_class.to_s.split('::').last
    bit_depth = /CRC(?<depth>\d*)/.match(parent_name)[:depth].to_i
    # Name for new class
    class_name = "My#{parent_name}".to_sym
    class_body = Class.new(parent_class)
    # Redefine TABLE constant
    class_body.class_eval("TABLE = #{self.get_table(bit_depth)}.freeze")
    Digest.const_set(class_name, class_body)
  end

